
# \" for double quotes
course_full_name = "Intro to \"Python Programing\""
print(course_full_name)
# \' for single quotes
course_full_name = "Intro to \'Python Programing\'"
print(course_full_name)
# \\ for adding backslash
course_full_name = " Intro to \\Python Programing\\"
print(course_full_name)
# \n fro new line
course_full_name = "Intro to \n Python Programing"
print(course_full_name)
